package entity;

public class buka {
	int rid;//补卡编号
	String rempid;//员工编号
	String rdate;//补卡时间
	String rreson;//负责人
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getRempid() {
		return rempid;
	}
	public void setRempid(String rempid) {
		this.rempid = rempid;
	}
	
	
	public String getRdate() {
		return rdate;
	}
	public void setRdate(String rdate) {
		this.rdate = rdate;
	}
	public String getRreson() {
		return rreson;
	}
	public void setRreson(String rreson) {
		this.rreson = rreson;
	}
	
	
}
