package entity;

public class department {
	String id;//部门id
	String decode;//部门编码
	String dename;//部门名字
	String deres;//部门负责人
	String deduty;//部门职责
	String deup;//上级部门
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getDecode() {
		return decode;
	}
	public void setDecode(String decode) {
		this.decode = decode;
	}
	public String getDename() {
		return dename;
	}
	public void setDename(String dename) {
		this.dename = dename;
	}
	public String getDeres() {
		return deres;
	}
	public void setDeres(String deres) {
		this.deres = deres;
	}
	public String getDeduty() {
		return deduty;
	}
	public void setDeduty(String deduty) {
		this.deduty = deduty;
	}
	public String getDeup() {
		return deup;
	}
	public void setDeup(String deup) {
		this.deup = deup;
	}
	
	

}
